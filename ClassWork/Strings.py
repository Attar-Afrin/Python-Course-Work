# Operations on Strings
#1 Concatination
str1 = "Hello"
str2 = "World"
result = str1 + " " + str2
print(result) #Hello World

#2 Repetition
print("Python " *3) #Python Python Python 
print("[a]"*5) #[a][a][a][a][a]


#3 Indexing
Language="English"
print(Language[5]) #s
print(Language[-1]) #h

#4 Slicing
print(Language[0:3]) #Eng
print(Language[:]) #English
print(Language[::-1]) #hsilgnE